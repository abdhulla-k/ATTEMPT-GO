package main

import "fmt"

func main() {
	// 	Go Format Verbs Table
	// Go fmt.Printf Format Verbs

	// | Data Type | Format Verb | Description | Example Output |
	// | -----------------------------------------------------------
	// | String | %s | Prints the value as a plain string. | GoLang |
	// | String | %q | Prints the string with double quotes. | "GoLang" |
	// | Integer | %d | Prints the value as a base 10 (decimal) integer. | 123 |
	// | Boolean | %t | Prints the boolean value (true or false). | true |
	// | Floating Point | %f | Prints the number in fixed-point notation. | 3.141590 |
	// | General | %v | The "default" format; prints the value in a natural way. | 123 or GoLang |
	// | Type | %T | Prints the data type of the value. | int or string |
	fmt.Println("Hello, World!")
}
